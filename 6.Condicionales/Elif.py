letra = "u"

if letra.lower() == "a":  #letra.lower es para cambiar la "a" por mayuscula y la tome como a o A
    print ("Esta vocal es la A")
elif letra.lower() == "e":
    print ("Esta vocal es la E")
elif letra.lower() == "i":
    print ("Esta vocal es la I")
elif letra.lower() == "o":
    print ("Esta vocal es la O")
else:
    print ("Esta vocal es la U")